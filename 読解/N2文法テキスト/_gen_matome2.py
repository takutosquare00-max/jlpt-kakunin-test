def options_html(qname, choices, correct_idx, explanation=''):
    items = ''
    for i, c in enumerate(choices, 1):
        v = '1' if i == correct_idx else '0'
        sid = f'{qname}{i}'
        items += (f'<div class="option">'
                  f'<input id="{sid}" name="{qname}" type="radio" value="{v}"/>'
                  f'<label for="{sid}"><span class="opt-key">{i}.</span>'
                  f'<span class="opt-body">{c}</span></label></div>')
