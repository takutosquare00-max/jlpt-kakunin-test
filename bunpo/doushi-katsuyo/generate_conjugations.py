# 五段活用: 辞書形の最後の文字 → 未然(あ段), 未然う(お段), 連用(い段), 連用た(促音便等), 終止, 連体, 仮定(え段), 命令(え段)
GODAN = {
    'う': ('わ', 'お', 'い', 'っ', 'う', 'う', 'え', 'え'),
    'く': ('か', 'こ', 'き', 'い', 'く', 'く', 'け', 'け'),
    'ぐ': ('が', 'ご', 'ぎ', 'い', 'ぐ', 'ぐ', 'げ', 'げ'),
    'す': ('さ', 'そ', 'し', 'し', 'す', 'す', 'せ', 'せ'),
    'つ': ('た', 'と', 'ち', 'っ', 'つ', 'つ', 'て', 'て'),
    'ぬ': ('な', 'の', 'に', 'ん', 'ぬ', 'ぬ', 'ね', 'ね'),
    'ぶ': ('ば', 'ぼ', 'び', 'ん', 'ぶ', 'ぶ', 'べ', 'べ'),
    'む': ('ま', 'も', 'み', 'ん', 'む', 'む', 'め', 'め'),
    'る': ('ら', 'ろ', 'り', 'っ', 'る', 'る', 'れ', 'れ'),
}

def conj_godan(verb):
    """五段活用の活用を返す"""
    stem = verb[:-1]
    last = verb[-1]
    if last not in GODAN:
        return None
    mizen, mizen_u, renyo, renyo_ta, shushi, rentai, katei, meirei = GODAN[last]
    if last == 'く' and verb == '行く':
        renyo_ta = 'っ'  # 行く→行った
    # た形: ぐ→いだ, ぬぶむ→ん+だ, く→いた, す→した, つうる→っ+た
    if last == 'ぐ':
        ta_form = stem + 'いだ'
    elif last in 'ぬぶむ':
        ta_form = stem + 'んだ'
    elif last == 'く':
        ta_form = stem + ('った' if verb == '行く' else 'いた')
    elif last == 'す':
        ta_form = stem + 'した'
    else:  # うつる
        ta_form = stem + 'った'
    return {
        'ない': stem + mizen + 'ない',
        'よう': stem + mizen_u + 'う',
        'ます': stem + renyo + 'ます',
        'た': ta_form,
        '辞書形': verb,
        'の': verb,
        'ば': stem + katei + 'ば',
        '命令': stem + meirei,
    }

def conj_ichidan(verb, is_upper):
    """上一段・下一段活用。is_upper: 上一段=True"""
    stem = verb[:-1]  # 見る→み, 食べる→たべ
    return {
        'ない': stem + 'ない',
        'よう': stem + 'よう',
        'ます': stem + 'ます',
        'た': stem + 'た',
        '辞書形': verb,
        'の': verb,
        'ば': stem + 'れば',
        '命令': stem + ('ろ' if is_upper else 'ろ'),
    }

def conj_suru(verb):
    """サ変活用。verbは「勉強する」形式"""
    stem = verb[:-2]  # 勉強する→勉強
    return {
        'ない': stem + 'しない',
        'よう': stem + 'しよう',
        'ます': stem + 'します',
        'た': stem + 'した',
        '辞書形': verb,
        'の': verb,
        'ば': stem + 'すれば',
        '命令': stem + 'しろ',
    }

def conj_kuru():
    """カ変活用"""
    return {
        'ない': '来ない',
        'よう': '来よう',
        'ます': '来ます',
        'た': '来た',
        '辞書形': '来る',
        'の': '来る',
        'ば': '来れば',
        '命令': '来い',
    }

def get_verb_type(nai_form, verb):
    """ない形から動詞の種類を判定"""
    if verb == '来る':
        return 'カ変'
    if verb.endswith('する') or 'する' in verb:
        return 'サ変'
    if nai_form == 'いない' and verb == '居る':
        return '下一段'
    if nai_form == 'ない' and verb == 'ある':
        return '五段'
    # ない形の直前の文字で判定
    if nai_form.endswith('ない'):
        before_nai = nai_form[:-2]  # 会わない→会わ
        if not before_nai:
            return '下一段'  # いる→いない
        last_char = before_nai[-1]
        if last_char in 'あいうえお' and last_char != 'い' and last_char != 'え':
            return '五段'
        if last_char == 'い':
            return '上一段'
        if last_char == 'え':
            return '下一段'
    return '五段'

def conjugate(verb, nai_form):
    """動詞を活用させる"""
    if verb == '来る':
        return conj_kuru()
    if verb.endswith('する'):
        return conj_suru(verb)
    # 特殊
    if verb == 'ある' and nai_form == 'ない':
        return {'ない': 'ない', 'よう': 'あろう', 'ます': 'あります', 'た': 'あった', '辞書形': 'ある', 'の': 'ある', 'ば': 'あれば', '命令': 'あれ'}
    if verb == '居る' and nai_form == 'いない':
        return {'ない': 'いない', 'よう': 'いよう', 'ます': 'います', 'た': 'いた', '辞書形': 'いる', 'の': 'いる', 'ば': 'いれば', '命令': 'いろ'}
    # え段（下一段）: 開ける→開けない、食べる→食べない、出る→出ない（で）
    EDAN = 'えけげせぜてでねへべめれ'
    # 読みで判定するため、漢字のない形も考慮。出ない→で、開けない→け
    before_nai = nai_form[:-2] if nai_form.endswith('ない') else ''
    last_sound = before_nai[-1] if before_nai else ''
    # 下一段: 出る(で), 開ける(け), 食べる(べ) - 語幹末がえ段
    if nai_form.endswith('ない') and last_sound in EDAN:
        return conj_ichidan(verb, False)
    # 出る、寝るは特別（漢字のため読みで判定）: 出ない(で)、寝ない(ね)
    if verb in ['出る', '寝る']:
        return conj_ichidan(verb, False)
    # 上一段: 着る, 見る, 起きる, 浴びる, 借りる, 降りる 等
    ichidan_upper = ['見る', '着る', '起きる', '浴びる', '借りる', '降りる']
    if verb in ichidan_upper:
        return conj_ichidan(verb, True)
    if verb == '要る':
        return conj_godan(verb)
    # 五段
    return conj_godan(verb)

